import mongoose from "mongoose";

const { Schema } = mongoose;

const postSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    desc: {
      type: String,
      required: true,
    },
    img: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
    username: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

let Blogpost;
try {
  Blogpost = mongoose.model("Blogpost");
} catch {
  Blogpost = mongoose.model("Blogpost", postSchema);
}

export default Blogpost;
